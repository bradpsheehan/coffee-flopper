  def review(shop)
    image_tag(shop.yelp.rating_img_url_large, class: "media-object")
  end

  def wifi_helper(shop)
    if shop.wifi == true
      "Has Free Wifi"
    else
      "No Free Wifi"
    end
  end

  def group_helper(shop)
